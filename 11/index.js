var userdata= prompt("please enter week day")
if (userdata==1) {
    console.log("Monday");
} else if (userdata==2) {
    console.log("Tuesday")
} else if (userdata==3) {
    console.log("Wednesday")
} else if (userdata==4) {
    console.log("Thursday")
} else if (userdata==5) {
    console.log("Friday")
} else if (userdata==6) {
    console.log("Saturday")
} else if (userdata==7) {
    console.log("Sunday")
}else   {
    console.log("Please enter a true value")
}